SELECT 
    E.EMPLOYEE_ID AS "Employee ID",
    E.EMPLOYEE_FIRST_NAME AS "First Name",
    E.EMPLOYEE_LAST_NAME AS "Last Name",
    V.VINEYARD_NAME AS "Managed Vineyard"
FROM 
    EMPLOYEE_T E
JOIN 
    VINEYARD_T V ON E.EMPLOYEE_ID = V.VINEYARD_MANAGER;

/*VIEW 3*/
CREATE VIEW v3Team2 AS
SELECT 
    E.EMPLOYEE_ID AS "Employee ID",
    E.EMPLOYEE_FIRST_NAME AS "First Name",
    V.VINEYARD_NAME AS "Managed Vineyard",
    H.HARVEST_YEAR AS "Harvest Year"
FROM 
    EMPLOYEE_T E
JOIN 
    VINEYARD_T V ON E.EMPLOYEE_ID = V.VINEYARD_MANAGER
JOIN 
    HARVEST_T H ON V.VINEYARD_ID = H.VINEYARD_NAME
WHERE 
    E.EMPLOYEE_POSITION = 'Wine Maker';
    
/*VIEW 4*/
CREATE VIEW v4Team2 AS
SELECT 
    E.EMPLOYEE_ID AS "Employee ID",
    E.EMPLOYEE_FIRST_NAME AS "First Name",
    V.VINEYARD_NAME AS "Managed Vineyard",
    V.VINEYARD_MANAGER AS "Vineyard Manager ID"
FROM 
    EMPLOYEE_T E
FULL OUTER JOIN 
    VINEYARD_T V ON E.EMPLOYEE_ID = V.VINEYARD_MANAGER;
    
/* VIEW 5*/
CREATE VIEW v5Team2 AS
SELECT
    C.CUSTOMER_ID AS "Customer ID",
    COUNT(PO.STATUS) AS "Total Shipped Orders"
FROM
    CUSTOMER_T C
LEFT JOIN
    PRODUCT_ORDER_T PO ON C.CUSTOMER_ID = PO.CUSTOMER_ID
WHERE
    PO.STATUS = 'Shipped'
GROUP BY
    C.CUSTOMER_ID;
    
/* VIEW 6*/
CREATE VIEW v6Team2 AS
SELECT
    W.WINE_NAME AS "Wine Name",
    W.WINE_VINTAGE_YEAR AS "Vintage Year",
    B.BOTTLE_COLOR AS "Bottle Color"
FROM
    WINE_T W
JOIN
    BOTTLE_T B ON W.WINE_ID = B.BOTTLE_ID;
    
/* VIEW 7*/
CREATE VIEW v7Team2 AS
SELECT
    C.CUSTOMER_ID AS "Customer ID",
    CASE
        WHEN I.CUST_ID IS NOT NULL THEN I.FIRST_NAME || ' ' || I.LAST_NAME
        WHEN B.BUSINESS_ID IS NOT NULL THEN B.BUSINESS_NAME
    END AS "Customer Name",
    CASE
        WHEN C.CUSTOMER_ID IS NOT NULL THEN C.CUSTOMER_STREET || ' ' || C.CUSTOMER_CITY || ' ' || C.CUSTOMER_STATE || ' ' || C.CUSTOMER_ZIP
    END AS "Customer Address",
    C.CUSTOMER_PHONE AS "Customer Phone",
    C.CUSTOMER_TYPE AS "Customer Type"
FROM
    CUSTOMER_T C
LEFT JOIN
    INDIVIDUAL_T I ON C.CUSTOMER_ID = I.CUST_ID
LEFT JOIN
    BUSINESS_T B ON C.CUSTOMER_ID = B.BUSINESS_ID;


