SELECT
    W.WINE_NAME,
    W.WINE_CATEGORY,
    V.VINEYARD_NAME,
    V.VINEYARD_MANAGER,
    H.HARVEST_YEAR,
    H.VINEYARD_NAME AS HARVEST_VINEYARD,
    H.HARVEST_RIPENESS
FROM
    WINE_T W
JOIN
    VINEYARD_T V ON W.WINE_MAKER_ID = V.VINEYARD_MANAGER
JOIN
    HARVEST_T H ON W.WINE_ID = H.GRAPE_TYPE
ORDER BY
    W.WINE_NAME, H.HARVEST_YEAR DESC;


